    integer(kind = 4), allocatable :: paramsToFit(:)

    real(kind = 8), allocatable :: y_output(:), solution(:), funVec(:), Jacobian(:,:), JTJ(:,:),JTWJ(:,:),paramReal(:)
    !real(kind = 8) :: tolX, tolFun, tolOpt, lambda0

